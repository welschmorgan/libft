#include "libft.h"

char	*ft_itoa(int n)
{
	int		sign;
	char	*str;

	str = ft_strnew(32);
	sign = (n < 0);
	if (n == 0)
		*(str++) = '0';
	else if (n < 0)
	{
		*(str++) = '-';
		n = -1;
	}
	while (n > 0)
	{
		*(str++) = '0' + (n % 10);
		n /= 10;
	}
	*str = '\0';
	return (str);
}
