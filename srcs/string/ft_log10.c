#include "libft.h"

static unsigned		g_log10s[][2] = {
	{ 1000000000, 10 },
	{ 100000000, 9 },
	{ 10000000, 8 },
	{ 1000000, 7 },
	{ 100000, 6 },
	{ 10000, 5 },
	{ 1000, 4 },
	{ 100, 3 },
	{ 10, 2 },
	{ 1, 1 }
};

unsigned			ft_log10u(unsigned x)
{
	int				i;
	int				cnt;

	cnt = sizeof(g_log10s) / sizeof(int[2]);
	i = 0;
	while (i < cnt)
	{
		if (x >= g_log10s[i][0])
			return (g_log10s[i][1]);
		i++;
	}
	return (0);
}
